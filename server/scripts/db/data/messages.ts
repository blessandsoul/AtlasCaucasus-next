// Function to get random message from category
export function getRandomMessage(category: string[]): string {
  return category[Math.floor(Math.random() * category.length)];
}

// Language-specific message pool maps
const INQUIRY_MESSAGES_BY_LANG = {
  ka: INQUIRY_MESSAGES,
  en: EN_INQUIRY_MESSAGES,
  ru: RU_INQUIRY_MESSAGES,
} as const;

const RESPONSE_MESSAGES_BY_LANG = {
  ka: RESPONSE_MESSAGES,
  en: EN_RESPONSE_MESSAGES,
  ru: RU_RESPONSE_MESSAGES,
} as const;

const FOLLOW_UP_MESSAGES_BY_LANG = {
  ka: FOLLOW_UP_MESSAGES,
  en: EN_FOLLOW_UP_MESSAGES,
  ru: RU_FOLLOW_UP_MESSAGES,
} as const;

// Function to generate a conversation thread
export function generateConversation(
  type: 'tour' | 'guide' | 'driver' | 'company',
  messageCount: number = 4,
  language?: 'ka' | 'en' | 'ru'
): string[] {
  const langs: Array<'ka' | 'en' | 'ru'> = ['ka', 'en', 'ru'];
  const lang = language ?? langs[Math.floor(Math.random() * langs.length)];

  const inquiryPool = INQUIRY_MESSAGES_BY_LANG[lang];
  const responsePool = RESPONSE_MESSAGES_BY_LANG[lang];
  const followUpPool = FOLLOW_UP_MESSAGES_BY_LANG[lang];

  const conversation: string[] = [];

  // Initial inquiry
  conversation.push(getRandomMessage(inquiryPool[type]));

  // Response
  if (messageCount > 1) {
    const responseType = Math.random() > 0.2 ? 'positive' : 'neutral';
    conversation.push(getRandomMessage(responsePool[responseType]));
  }

  // Follow-ups
  for (let i = 2; i < messageCount; i++) {
    if (i % 2 === 0) {
      conversation.push(getRandomMessage(followUpPool.traveler));
    } else {
      conversation.push(getRandomMessage(followUpPool.provider));
    }
  }

  return conversation;
}
